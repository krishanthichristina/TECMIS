        public void showRecord(){
    
        try {
            String cID = (String) combo_cID.getSelectedItem();
            String studentID = userID;
            String session = (String) com_type.getSelectedItem();
            
            String query = "SELECT AttID, Date, Session, Status FROM attendance WHERE CourseID = ? AND userID = ? AND Session = ?";
            PreparedStatement pstmt = connection.prepareStatement(query);
            pstmt.setString(1, cID);
            pstmt.setString(2, studentID);
            pstmt.setString(3, session);
            ResultSet rs = pstmt.executeQuery();

            DefaultTableModel model = (DefaultTableModel) tbl_showAtt.getModel();
            model.setRowCount(0);

            int presentCount = 0;
            int totalCount = 0;

            while (rs.next()) {
                System.out.println("ssssss");
                Integer attID = rs.getInt("AttID");
                String date = rs.getString("Date");
                String type = rs.getString("Session");
                String status = rs.getString("Status");
                model.addRow(new Object[]{attID, date, type, status});

                totalCount++;
                if (status.equals("Present")) {
                    presentCount++;
                }
            }

            tbl_showAtt.setModel(model);

            if (totalCount > 0) {
                int precentage = (int) (((double) presentCount / totalCount) * 100);
                prog_precentage.setValue(precentage);
                lbl_precentage.setText("Attendance percentage: " + precentage + "%");
            } else {
                prog_precentage.setValue(0);
                lbl_precentage.setText("Attendance percentage: N/A");
            }
        } catch (Exception e) {
            System.out.println(e);
        }
        
    }
