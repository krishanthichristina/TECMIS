    public void showCourse(){
    
        String stLevel = "";
        String depID = "";
        
        try {
            Statement stmt = connection.createStatement();
            String sql = " SELECT LevelSem, depID FROM userprofiles WHERE id = '" + userID + "'";
            ResultSet rs = stmt.executeQuery(sql);
            
            if (rs.next()) {
                stLevel = rs.getString("LevelSem");
                depID = rs.getString("depID");
            }
            
            rs.close();
            stmt.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }
        
        try {
            Statement stmt = connection.createStatement();
            String sql = "SELECT CourseID, CourseName, Credit FROM coursedetails WHERE LevelSem = '" + stLevel + "' AND DepID = '" + depID + "'";
            ResultSet rs = stmt.executeQuery(sql);
            
            DefaultTableModel model = (DefaultTableModel) courseJTable.getModel();
            model.setRowCount(0);
            
            while (rs.next()) {
                Object[] row = new Object[3];
                row[0] = rs.getString("CourseID");
                row[1] = rs.getString("CourseName");
                row[2] = rs.getInt("Credit");
                model.addRow(row);
            }
            
            rs.close();
            stmt.close();
        } catch (SQLException e) {
            e.printStackTrace();
        }    
    
    }
    
    
