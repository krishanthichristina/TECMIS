    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
    String selectedDepartment = (String) DepType.getSelectedItem();

    String query = "SELECT DISTINCT  a.userID AS StudentID, u.name AS StudentName, '' AS Sign FROM attendance a INNER JOIN userprofiles u ON a.userID = u.id WHERE a.Department = ?";

try (PreparedStatement pst = connection.prepareStatement(query)) {
    pst.setString(1, selectedDepartment);
    ResultSet rs = pst.executeQuery();

    DefaultTableModel model = new DefaultTableModel(new String[]{"No", "StudentID", "Student Name", "Sign"}, 0);
    JTable attendentSheet = new JTable(model);

    int count = 1; // Counter for the "No" column

    while (rs.next()) {
        String studentID = rs.getString("StudentID");
        String studentName = rs.getString("StudentName");

        Object[] rowData = {count++, studentID, studentName, ""}; // Increment the count
        model.addRow(rowData);
    }

    AttendentSheet.setModel(model);
} catch (Exception ex) {
    ex.printStackTrace();
}

    }//GEN-LAST:event_jButton1ActionPerformed

